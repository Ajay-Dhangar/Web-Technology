public class Variables {
    public static void main(String[] args) {
        String name = "Ajay";
        System.out.println(name);
        int myNum = 15;
        System.out.println(myNum);
        myNum = 20;  // myNum is now 20
        System.out.println(myNum);
        int x = 5;
        int y = 6;
        System.out.println(x + y); // Print the value of x + y
    }
    
}
